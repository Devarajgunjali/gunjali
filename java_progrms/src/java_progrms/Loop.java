package java_progrms;

public class Loop {
	public static void main(String args[]){
		int i =1;
		while(i<=10){
			System.out.println(i);
	        i=i+1;
		}
	


//
for(int j=1;j<=10;j++){
	System.out.println(j);
}

// 10 to 1
for(int k=10; k>=1; k--){
	System.out.println(k);
	
}
for(int h=10; h>=-10; h--){
	System.out.println(h);
}

	
}

}